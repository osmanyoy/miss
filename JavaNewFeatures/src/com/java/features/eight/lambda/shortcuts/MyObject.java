package com.java.features.eight.lambda.shortcuts;

public class MyObject {
	private String myStr;

	public MyObject(final String myStr) {
		super();
		this.myStr = myStr;
	}

	public String getMyStr() {
		return this.myStr;
	}

	public void setMyStr(final String myStr) {
		this.myStr = myStr;
	}

}
